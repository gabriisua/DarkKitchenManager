    public static string GenerateZpl(
        PlateResponseDto plate, 
        NutritionalSummaryDto nutrition, 
        string allergensInTraces, 
        string lotNumber, 
        DateTime productionDate, 
        int totalCopies = 1,
        int pauseAfter = 0,
        DateTime? customExpiryDate = null,
        bool isWow = false,
        bool isXl = false,
        decimal? customWeight = null)
    {
        if (totalCopies <= 0) totalCopies = 1;

        decimal totalWeightDecimal = customWeight ?? plate.Ingredients.Sum(i => i.WeightInGrams);
        var totalWeight = totalWeightDecimal.ToString("0");
        
        var finalExpiryDate = customExpiryDate ?? productionDate.AddDays(plate.DaysToExpire);
        var expiryDate = finalExpiryDate.ToString("dd/MM/yyyy");
        
        var ingredientiList = plate.Ingredients.Select(i => 
            string.IsNullOrWhiteSpace(i.SubIngredients) ? i.IngredientName : $"{i.IngredientName} ({i.SubIngredients})"
        );
        var ingredientiString = string.Join(", ", ingredientiList) + ".";

        var energiaKcal100g = nutrition.EnergyKcal;
        var energiaKj = (energiaKcal100g * 4.184m).ToString("0");
        var grassi = nutrition.Fats.ToString("0.0");
        var grassiSaturi = nutrition.SaturatedFats.ToString("0.0");
        var carboidrati = nutrition.Carbohydrates.ToString("0.0");
        var zuccheri = nutrition.Sugars.ToString("0.0");
        var fibre = nutrition.Fibers.ToString("0.0");
        var proteine = nutrition.Proteins.ToString("0.0");
        var sale = nutrition.Salt.ToString("0.00");

        var totalKcal = ((energiaKcal100g * totalWeightDecimal) / 100).ToString("0");

        var sb = new StringBuilder();
        
        sb.Append(@"^XA");
        sb.Append(@"^PON");
        sb.Append(@"^CI28");
        sb.Append(@"^PW480");  
        sb.Append(@"^LL2032"); 
        sb.Append(@"^FWN");    
        
        sb.Append($@"^FO40,260^A0N,32,32^FB400,3,0,C^FD{plate.Name}^FS");
        sb.Append($@"^FO40,460^A0N,28,28^FB120,1,0,C^FD{totalKcal}^FS");

        string heatingTime = (!plate.MicrowaveMinutes.HasValue || plate.MicrowaveMinutes.Value <= 0) 
            ? @"0/30\&Sec" 
            : $@"2/3\&Min";

        sb.Append($@"^FO245,490^A0N,23,23^FB120,2,0,C^FD{heatingTime}^FS");
        sb.Append($@"^FO40,640^A0N,32,32^FB400,3,0,C^FD{plate.Name}^FS");

        string selectedIconZpl = plate.DietaryIcon switch
        {
            DietaryIconType.Meat => ICON_MEAT,
            DietaryIconType.Fish => ICON_FISH,
            DietaryIconType.Vegetarian => ICON_VEG,
            DietaryIconType.Vegan => ICON_VEGAN,
            _ => ""
        };

        if (!string.IsNullOrEmpty(selectedIconZpl))
        {
            sb.Append($@"^FO05,785{selectedIconZpl}");
        }

        if (isXl)
        {
            sb.Append($@"^FO310,788{ICON_XL}");
        }
        else if (isWow)
        {
            sb.Append($@"^FO310,788{ICON_WOW}");
        }

        int currentX = 40;      
        int currentY = 965; 
        int descFont = 18;      
        int lineStep = 20;      
        int sectionGap = 12;    
        int maxChars = 42; 

        var ingLines = SplitToLines("Ingredienti: " + ingredientiString, maxChars); 
        foreach (var line in ingLines)
        {
            sb.Append($@"^FO{currentX},{currentY}^A0N,{descFont},{descFont}^FD{line}^FS");
            currentY += lineStep; 
        }
        currentY += sectionGap; 

        var allLines = SplitToLines("Allergeni: " + allergensInTraces, maxChars);
        foreach (var line in allLines)
        {
            sb.Append($@"^FO{currentX},{currentY}^A0N,{descFont},{descFont}^FD{line}^FS");
            currentY += lineStep;
        }

        var tracceLines = SplitToLines("Tracce: " + allergensInTraces, maxChars);
        foreach (var line in tracceLines)
        {
            sb.Append($@"^FO{currentX},{currentY}^A0N,{descFont},{descFont}^FD{line}^FS");
            currentY += lineStep;
        }
        currentY += sectionGap;

        var nutLines = SplitToLines($"Valori medi per 100g di prodotto: {energiaKj}kJ - {energiaKcal100g}kcal - Grassi {grassi}gr - di cui acidi grassi saturi {grassiSaturi}gr - Carboidrati {carboidrati}gr - di cui zuccheri {zuccheri}gr - Fibre {fibre}gr - Proteine {proteine}gr - Sale {sale}gr", maxChars);
        foreach (var line in nutLines)
        {
            sb.Append($@"^FO{currentX},{currentY}^A0N,{descFont},{descFont}^FD{line}^FS");
            currentY += lineStep;
        }

        int footerY = 1750;
        int smallFont = 18;
        
        sb.Append($@"^FO{currentX},{footerY}^A0N,{smallFont},{smallFont}^FDConfezionato in atmosfera protettiva^FS");
        sb.Append($@"^FO{currentX},{footerY + 20}^A0N,{smallFont},{smallFont}^FDConservare in frigorifero a temperature inferiore a +4 C^FS");
        sb.Append($@"^FO{currentX},{footerY + 50}^A0N,{smallFont},{smallFont}^FB420,2,0,L^FDProdotto per Foorban SRL nello stabilimento n: IT 00001 CEE E9E2U ^FS");

        int infoY = footerY + 110;
        sb.Append($@"^FO40,{infoY}^A0N,20,20^FD{totalWeight}gr^FS");
        sb.Append($@"^FO200,{infoY}^A0N,20,20^FDLotto: {lotNumber}^FS");
        sb.Append($@"^FO180,{infoY + 30}^A0N,22,22^FDScadenza: {expiryDate}^FS");

        sb.Append($@"^PQ{totalCopies},{pauseAfter}");
        sb.Append(@"^XZ");

        return sb.ToString();
    }
    
    private static List<string> SplitToLines(string text, int maxChars)
    {
        var lines = new List<string>();
        var words = text.Split(' ');
        var line = "";

        foreach (var w in words)
        {
            if ((line + w).Length > maxChars)
            {
                lines.Add(line.Trim());
                line = w + " ";
            }
            else line += w + " ";
        }

        if (!string.IsNullOrWhiteSpace(line))
            lines.Add(line.Trim());

        return lines;
    }
